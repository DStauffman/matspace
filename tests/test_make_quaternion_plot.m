        function test_use_mean(self)
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, self.time_one, self.quat_one, ...
                self.time_two, self.quat_two, UseMean=true, FigVisible=self.fig_visible);
        end

        function test_no_rms_in_legend(self)
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, self.time_one, self.quat_one, ...
                self.time_two, self.quat_two, UseMean=true, ShowRms=false, FigVisible=self.fig_visible);
        end

        function test_plot_zero(self)
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, self.time_one, self.quat_one, ...
                self.time_two, self.quat_two, PlotZero=true, FigVisible=self.fig_visible);
        end

        function test_disp_bounds(self)
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, self.time_one, self.quat_one, ...
                self.time_two, self.quat_two, DispXMin=2, DispXMax=5, FigVisible=self.fig_visible);
        end

        function test_no_overlap(self)
            time_one = 0:10;
            time_two = 2.5:12.5;
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, time_one, self.quat_one, ...
                time_two, self.quat_two, FigVisible=self.fig_visible);
        end

        function test_none1(self)
            self.fig_hand = matspace.plotting.make_quaternion_plot(self.description, self.time_one, self.quat_one, ...
                zeros(1, 0), zeros(4, 0), FigVisible=self.fig_visible);
        end

        function test_none2(self)
