classdef test_get_gps_to_utc_offset < matlab.unittest.TestCase

    % Tests the matspace.gps.gps_to_datetime function with the following cases:
    %     Recent
    %     Bounds
    %     Close changes

    methods (Test)
        function test_recent(self)
            gps_days = days(datetime('now') - datetime(1980, 1, 6));
            gps_to_utc_offset = matspace.gps.get_gps_to_utc_offset(gps_days);
            self.verifyEqual(gps_to_utc_offset, -18);
        end
        function test_bounds(self)
            date_offset = {...
                {datetime(1980, 3, 1),   0}, {datetime(1980, 9, 1),   0}, ...
                {datetime(1981, 3, 1),   0}, {datetime(1981, 9, 1),  -1}, ...
                {datetime(1982, 3, 1),  -1}, {datetime(1982, 9, 1),  -2}, ...
                {datetime(1983, 3, 1),  -2}, {datetime(1983, 9, 1),  -3}, ...
                {datetime(1984, 3, 1),  -3}, {datetime(1984, 9, 1),  -3}, ...
                {datetime(1985, 3, 1),  -3}, {datetime(1985, 9, 1),  -4}, ...
                {datetime(1986, 3, 1),  -4}, {datetime(1986, 9, 1),  -4}, ...
                {datetime(1987, 3, 1),  -4}, {datetime(1987, 9, 1),  -4}, ...
                {datetime(1988, 3, 1),  -5}, {datetime(1988, 9, 1),  -5}, ...
                {datetime(1989, 3, 1),  -5}, {datetime(1989, 9, 1),  -5}, ...
                {datetime(1990, 3, 1),  -6}, {datetime(1990, 9, 1),  -6}, ...
                {datetime(1991, 3, 1),  -7}, {datetime(1991, 9, 1),  -7}, ...
                {datetime(1992, 3, 1),  -7}, {datetime(1992, 9, 1),  -8}, ...
                {datetime(1993, 3, 1),  -8}, {datetime(1993, 9, 1),  -9}, ...
                {datetime(1994, 3, 1),  -9}, {datetime(1994, 9, 1), -10}, ...
                {datetime(1995, 3, 1), -10}, {datetime(1995, 9, 1), -10}, ...
                {datetime(1996, 3, 1), -11}, {datetime(1996, 9, 1), -11}, ...
                {datetime(1997, 3, 1), -11}, {datetime(1997, 9, 1), -12}, ...
                {datetime(1998, 3, 1), -12}, {datetime(1998, 9, 1), -12}, ...
                {datetime(1999, 3, 1), -13}, {datetime(1999, 9, 1), -13}, ...
                {datetime(2000, 3, 1), -13}, {datetime(2000, 9, 1), -13}, ...
                {datetime(2001, 3, 1), -13}, {datetime(2001, 9, 1), -13}, ...
                {datetime(2002, 3, 1), -13}, {datetime(2002, 9, 1), -13}, ...
                {datetime(2003, 3, 1), -13}, {datetime(2003, 9, 1), -13}, ...
                {datetime(2004, 3, 1), -13}, {datetime(2004, 9, 1), -13}, ...
                {datetime(2005, 3, 1), -13}, {datetime(2005, 9, 1), -13}, ...
                {datetime(2006, 3, 1), -14}, {datetime(2006, 9, 1), -14}, ...
                {datetime(2007, 3, 1), -14}, {datetime(2007, 9, 1), -14}, ...
                {datetime(2008, 3, 1), -14}, {datetime(2008, 9, 1), -14}, ...
                {datetime(2009, 3, 1), -15}, {datetime(2009, 9, 1), -15}, ...
                {datetime(2010, 3, 1), -15}, {datetime(2010, 9, 1), -15}, ...
                {datetime(2011, 3, 1), -15}, {datetime(2011, 9, 1), -15}, ...
                {datetime(2012, 3, 1), -15}, {datetime(2012, 9, 1), -16}, ...
                {datetime(2013, 3, 1), -16}, {datetime(2013, 9, 1), -16}, ...
                {datetime(2014, 3, 1), -16}, {datetime(2014, 9, 1), -16}, ...
                {datetime(2015, 3, 1), -16}, {datetime(2015, 9, 1), -17}, ...
                {datetime(2016, 3, 1), -17}, {datetime(2016, 9, 1), -17}, ...
                {datetime(2017, 3, 1), -18}, {datetime(2017, 9, 1), -18}, ...
                {datetime(2018, 3, 1), -18}, {datetime(2018, 9, 1), -18}, ...
            };
            for i = 1:length(date_offset)
                this_date = date_offset{i}{1};
                offset = date_offset{i}{2};
                gps_days = days(this_date - datetime(1980, 1, 6));
                gps_to_utc_offset = matspace.gps.get_gps_to_utc_offset(gps_days);
                self.verifyEqual(gps_to_utc_offset, offset, ['For date ',char(this_date)]);
            end
        end

        function test_close_changes(self)
            gps_days_array = [13509.999999, 13510.0, 13510.0 + 15 / 86400, 13510.0 + 20 / 86400];
            gps_to_utc_offset = matspace.gps.get_gps_to_utc_offset(gps_days_array);
            exp = [-17, -17, -17, -18];
            self.verifyEqual(gps_to_utc_offset, exp);
        end
    end
end